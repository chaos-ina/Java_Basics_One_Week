package day3;

public class Sample305 {
    public static void main(String[] args) {
        int num = 2;
        switch(num)
        {
            case 1:
                System.out.println("1!");
                break;
            case 2:
                System.out.println("2!");
                break;
            default:
                System.out.println("Switch Tutorial");
                break;
        }
    }   
}
