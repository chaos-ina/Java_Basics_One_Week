package day6;

public class SampleClass01 {
    // ==========================================================
    // 解説 (Explanation)
    // ==========================================================
    // クラスは「設計図」です。
    // ここでは「n」という数字を1つ持つだけのシンプルな設計図を作っています。

    // フィールド (Field): データの入れ物
    // finalがついているので、一度入れたら変更できません
    final int n;

    // コンストラクタ (Constructor): 初期化のための特別なメソッド
    // 「new SampleClass01(20)」のように書くと、ここが実行されます。
    public SampleClass01(int num) {
        this.n = num; // 受け取った num を、自分自身の n (this.n) に代入
    }

    // メソッド (Method): 動作
    public void show() {
        System.out.println("n = " + this.n);
    }

    // ==========================================================
    // やってみよう (Try this)
    // ==========================================================
    // 1. 新しいフィールド int m; を追加してみましょう
    // 2. コンストラクタで m にも値を入れるように修正してみましょう
    // 3. showメソッドで m の値も表示するようにしてみましょう
}