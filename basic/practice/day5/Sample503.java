package day5;

public class Sample503 {
    public static void main(String[] args) {
        // データ型 配列名[] = {値1, 値2, 値3, ...}; で初期化
        int n[] = {1, 2, 3, 4,   5};
        for(int i = 0; i < n.length; i++)
        {
            System.out.println("n[" + i + "] = " + n[i]);
        }
    }   
}
